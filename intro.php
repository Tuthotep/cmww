<p class="w3-text-teal w3-large" style="font-family:Permanent Marker">

La Masa Crítica, o BiciCrítica, es un fenómeno ciclista que​ celebramos todos
los meses, en numerosas ciudades alrededor del mundo.<br>

Su finalidad es introducir la movilidad sostenible en el debate público,
fomentar el uso de la bicicleta como transporte ecológico y sustentable, y,
en fin, reclamar el espacio público que la cultura del coche nos ha usurpado.<br>

Uno de los iconos de la movilización ciclista es sin duda la Masa Crítica.<br>
La Masa Critica, o Critical Mass, es una celebración reivindicativa de
la bicicleta, que se dá forma y materializa como el paseo multitudinario de ciclistas reivindicativos.<br><br>


<div class="w3-text-teal w3-sand w3-center" style="font-family:Permanent Marker">
Invitamos a todas las comunidades ciclistas de las ciudades alrededor del mundo a organizar y convocar su propia Masa Critica reinvidicativa.
</div>
<br><br>
</p>

<p class="w3-text-teal w3-large" style="font-family:Permanent Marker">

La Masa tiene lugar de forma periódica en innumerables ciudades del mundo; Empezó
en EEUU en 1992 y desde entonces se ha convertido en un movimiento que ha vivido una intensaexpansión transnacional (presente en más de 300 ciudades de los 5 continentes), interactuandointensamente con las características locales de cada una de las ciudades que les dá ímpetu.
El impacto de esta cita periódica ha sido gigante: ha reubicado la bicicleta como un elementode debate público y ha puesto en contacto a miles de ciclistas, muchos de ellos protagonistas
de nuevas y diversas iniciativas pro-bici en la ciudades. También ha conectado la cuestión dela movilidad sostenible con otras esferas de movilización, incorporando un nuevo elemento en elrepertorio de los movimientos sociales.
</p>